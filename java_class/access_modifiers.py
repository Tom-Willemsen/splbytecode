class ACC_COMMON(object):
    PUBLIC = 0x0001
    PRIVATE = 0x0002
    PROTECTED = 0x0004
    STATIC = 0x0008
    FINAL = 0x0010
    SYNTHETIC = 0x1000
    ENUM = 0x4000
    ABSTRACT = 0x0400


class ACC_CLASS(ACC_COMMON):
    INTERFACE = 0x0200
    ANNOTATION = 0x2000
    SUPER = 0x0020


class ACC_FIELD(ACC_COMMON):
    VOLATILE = 0x0040
    TRANSIENT = 0x0080


class ACC_METHOD(ACC_COMMON):
    SYNCHRONIZED = 0x0020
    BRIDGE = 0x0040
    VARARGS = 0x0080
    NATIVE = 0x0100
    STRICT = 0x0800
